vim.keymap.set("n", "qq", ":q<CR>", { desc = "Quit NeoVim" })
vim.keymap.set("n", "q1", ":q!<CR>", { desc = "Quit NeoVim without saving changes" })
vim.keymap.set("n", "qa", ":qa<CR>", { desc = "Quit NeoVim by closing all buffers" })
vim.keymap.set("n", "q2", ":qa!<CR>", { desc = "Quit NeoVim by closing all buffers without saving changes" })
vim.keymap.set("n", "<Esc>", ":nohlsearch<CR>", { desc = "Remove Search Highlights" })
vim.keymap.set("n", "<C-h>", "<C-w><C-h>", { desc = "Move cursor left" })
vim.keymap.set("n", "<C-l>", "<C-w><C-l>", { desc = "Move cursor right" })
vim.keymap.set("n", "<C-j>", "<C-w><C-j>", { desc = "Move cursor down" })
vim.keymap.set("n", "<C-k>", "<C-w><C-k>", { desc = "Move cursor up" })
vim.keymap.set("n", "qw", ":w<CR>", { desc = "Write current buffer to disk" })
vim.keymap.set("n", "qe", ":w<CR>:q<CR>", { desc = "Write and Quit NeoVim" })
vim.keymap.set("n", "sv", "<C-w>v", { desc = "Vertical Split" })
vim.keymap.set("n", "sh", "<C-w>s", { desc = "Horizontal Split" })
vim.keymap.set("n", ";", ":", { desc = "Command Mode" })
